void lcd_empty() {
		lcd_print(1,1, "                       ");
		lcd_print(2,1, "                       ");	
	
}


void printToTheLCD() {
		
		switch(uartMessage[0]) {
			case 'm': {
					lcd_empty();
					sprintf(lcdString, " %.2f", soilHumidity);//int i string yapiyor
					lcd_print(1,1, " Moisture(%) ");
					lcd_print(2, 1, lcdString);	
					break;
				
			}
			
			case 'w': {
				lcd_empty();
				sprintf(lcdString, " %.2f", waterLevel);
				lcd_print(1,1, " Water Tank(%): ");
				lcd_print(2,1, lcdString);
				break;
				
			}
			
		}
		
		
		
	
}

void checkMoisture() {
	
			if (soilHumidity > 10 && soilHumidity < 50) {
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, GPIO_PIN_RESET);
			moisture_pin = GPIO_PIN_15;

			}else {
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_RESET);
			moisture_pin = GPIO_PIN_14;

			}
			
			if (waterLevel > 40) {
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);
			water_pin = GPIO_PIN_3;

			}else {
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_3, GPIO_PIN_RESET);
			water_pin = GPIO_PIN_4;

			}
}
	


void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	
	
			if (hadc->Instance == ADC1) {
				
				adcMoistureValue = adcBuffer[0];
				adcWaterValue = adcBuffer[1];
				
				waterLevel = (100*((float)adcWaterValue/4096));
				soilHumidity = (100*((float)adcMoistureValue/4096));
				

				if(waterLevel < 40){
					HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5,1);
					if(waterLevel < 20){
							HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6,1);
						__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 1000);
					}else{
						__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 500);
					}
				}else{
					HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5,0);
					HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6,0);
					__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 0);

				}
			}

}


void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	
		HAL_UART_Receive_IT(&huart1, uartMessage, sizeof(uartMessage));
	
		printToTheLCD();

	
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
		
		printToTheLCD();
		checkMoisture();
		
		HAL_GPIO_TogglePin(GPIOB, moisture_pin);
		HAL_GPIO_TogglePin(GPIOA, water_pin);

   
}

